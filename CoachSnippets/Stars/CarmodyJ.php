  <div class="coach_body">
    <p>With the exception of a few misguided years a while ago, John has lived on the ocean his entire life.
      He has been in and around most every type of boat, tending towards the wind and human powered types.
    </p>
    <p>
      Almost 20 years ago, the sea kayak became his vessel of choice, and he has enjoyed adapting his “Big Boat”
      skills to kayaks.  John’s love of the traditions of the sea is infectious as he passes his seamanship
      and navigation skills on to his students.
    </p>
    <p>As a Coach, his understanding of the human body and biomechanics gives him unique insights into what will make his students more efficient, safer from injuries and better able to enjoy these centuries old craft in more exposed environments.
      John has led trips throughout Maine, the Canadian Maritimes, and the British Isles. He regularly instructs
      at Kayak Symposiums throughout the US.
    </p>
