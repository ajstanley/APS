       <p>Mark Scriver - (Ottawa, ON) White Water Canoe and SUP Coach</p>
  </div>

  <div class="coach_body">
    <p>Mark began his paddling career at summer camps in Ontario.
      He taught at Kandalore Outdoor centre and moved on to Black Feather Wilderness Adventures in the mid 80's
      where he continues to guide a trip a year in the Canadian Arctic or Greenland. He has also worked in the
      paddle sport industry as a retail buyer, sales representative and canoe designer and currently represents
      many leading paddle sport brands.
    </p>
    <p>He has authored and co-authored several books including,
      Thrill of the Paddle, Canoe Tripping; an Essential Guide and the
      Black Feather Camp Cookbook. He competed in white water freestyle for over a decade culminating
      in hosting the world championships and winning a gold medal. Though best know as a white water canoeist,
      he enjoys paddling all crafts and SUP is his latest interest.
    </p>
