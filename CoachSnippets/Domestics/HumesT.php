       <p>Tim Humes (New Brunswick) Canoe Coach</p>
  </div>

  <div class="coach_body">
    <p>
      Tim Humes began paddling (badly, by his own admission) in 1972 on the Miramichi. Since then he
      has established his outdoor retail business-Eddy Out Depot- while taking Paddle Canada/Canoe Kayak
      New Brunswick courses to qualify him as a FW Instructor , LW  Instructor, MW  Instructor,
      Sea Kayak 1 Instructor and Poling Instructor.
    </p>
    <p>
      Since his retirement from social work in '05, he has taken an even more active role in promoting
      safe recreational paddling through schools and clubs. He is served several executive roles with
      CKNB, presently as Past-President of CKNB. He is co-chair of the CKNB Heritage Committee and a
      prime mover behind the restoration of some ancient native portage trails in New Brunswick.
    </p>
