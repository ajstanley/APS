  <div  class="coach_body">
    <p>Richard is Past President of Paddle Canada. He is a level 4 Instructor and Instructor Trainer in the Sea Kayak program,
      holds multiple instructor designations in the canoe program and is the author of Paddle Canada’s Coastal Canoe Program.
    </p>
    <p>A graduate of the College of the North Atlantic’s Outdoor Recreation program, Richard has many years of wilderness tripping
      under his belt including a sea kayak expedition up the west coast of Greenland that finished 400 miles north of the Arctic Circle.
    </p>
    <p>
      For the last decade Richard has had the pleasure of exposing hundreds of people to the joys of paddling as the owner and course director
      of the Newfoundland Kayak Company.
    </p>

